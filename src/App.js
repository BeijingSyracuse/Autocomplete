let sorted = fruit.sort((a, b) => b.localeCompare(a));

export default function App() {
  const [list, setList] = useState(fruit);

  const handleInput = (e) => {
    setList(
      sorted.filter((ele) =>
        ele.toLowerCase().includes(e.target.value.toLowerCase())
      )
    );
  };
  // 加sensitive，tolowercase
  //加sorted前   fruit.filter((ele)  b.localeCompare(a)倒序

  return (
    <div className="App">
      <input type="text" placeholder="search" onChange={handleInput} />
      <ul>{list && list.map((item, index) => <li key={index}>{item}</li>)}</ul>
    </div>
  );
}
